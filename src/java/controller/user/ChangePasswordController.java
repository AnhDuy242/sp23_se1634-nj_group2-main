    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("views/user/ChangePassword.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       String n = request.getParameter("new");
       String r = request.getParameter("retype");
//       Account ac = (Account) request.getSession();

       if(!n.equals(r)) {
           request.setAttribute("No", "Change password failed");
           request.getRequestDispatcher("views/user/ChangePassword.jsp").forward(request, response);
       } else {
           DAO da = new DAO();
           Account a = new Account(((User)request.getSession().getAttribute("user")).getEmail(),n);
//           202cb962ac59075b964b07152d234b70
           try {
               da.Update(a);
           } catch (NoSuchAlgorithmException ex) {
               Logger.getLogger(ChangePasswordController.class.getName()).log(Level.SEVERE, null, ex);
           }
            request.setAttribute("Yes", "Change password successful!");
           request.getRequestDispatcher("views/user/ChangePassword.jsp").forward(request, response);
       }
    }

